# 地震のマグニチュードとエネルギーの関係
# マグニチュードが1増えるとエネルギーは32倍になる
# マグニチュードAの地震のエネルギーはマグニチュードBの地震のエネルギーの何倍か

# 入力を受け取る
A, B = map(int, input().split())

# マグニチュードAの地震のエネルギーはマグニチュードBの地震のエネルギーの32^(A-B)倍
# A > B なら A の方がエネルギーが大きい
# A < B なら B の方がエネルギーが大きい
# A = B なら同じエネルギー

energy_ratio = 32 ** (A - B)

print(energy_ratio)